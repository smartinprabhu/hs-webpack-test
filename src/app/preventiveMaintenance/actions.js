export function getPPMCalendarInfo(company, model, start, end, schedule, performedBy, priorities, category, types, isInspection) {
  let payload = `from_date=${start}&to_date=${end}`;

  const ppmList = ['Inspection Checklist', 'Smart Logger'];

  if (priorities && priorities.length) {
    payload = `${payload}&by_priority=${JSON.stringify(priorities)}`;
  }
  if (schedule && schedule.length) {
    payload = `${payload}&by_schedule=${JSON.stringify(schedule)}`;
  }
  if (performedBy && performedBy.length) {
    payload = `${payload}&performed_by=${performedBy}`;
  }
  if (category && category.length) {
    payload = `${payload}&type=${JSON.stringify(category)}`;
  }

  if (types && types.length) {
    payload = `${payload}&scheduler_type=${JSON.stringify(types)}`;
  } else if (isInspection) {
    payload = `${payload}&scheduler_type=${JSON.stringify(ppmList)}`;
  }

  let modelName = 'ppm_viewer';
  if (isInspection) {
    modelName = 'ppm_checklist';
  }

  return {
    [CALL_API]: {
      endpoint: `${modelName}/list?${payload}`,
      types: [GET_PPM_INFO, GET_PPM_INFO_SUCCESS, GET_PPM_INFO_FAILURE],
      method: 'GET',
      payload,
    },
  };
}

export function getAssetCategoryInfo(company, model, keyword, limit, offset) {
  let payload = 'domain=[';
  if (keyword && keyword.length > 0) {
    payload = `${payload}["name","ilike","${keyword}"]`;
  }
  payload = `${payload}]&model=${model}&fields=["name"]&limit=${limit || 200}&offset=${offset || 0}`;
  return {
    [CALL_API]: {
      endpoint: `search_read?${payload}`,
      types: [GET_ASSET_CATEGORIES_INFO, GET_ASSET_CATEGORIES_INFO_SUCCESS, GET_ASSET_CATEGORIES_INFO_FAILURE],
      method: 'GET',
      payload,
    },
  };
}

