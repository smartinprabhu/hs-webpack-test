export const NoResultImage = () => {
  const { themes } = useTheme(); // Access the current theme from your theme context

  if (themes === 'light') {
    return NoData; // Replace with the actual path to your light mode image
  }

  // Existing logic for other themes
  switch (appConfig.THEME) {
    case 'Blue':
      return NoResult_blue;
    case 'Green':
      return NoResult_green;
    case 'Prussian Blue':
      return NoResult_blue;
    default:
      return NoResult_green; // Default image
  }
};
